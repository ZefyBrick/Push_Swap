#include "libft.h"

char	*ft_strcat(char *d, const char *s)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	j = ft_strlen(d);
	while (s[i] != '\0')
		d[j++] = s[i++];
	d[j] = '\0';
	return (d);
}
