#include "push_swap.h"

void		red(void)
{
	write(1, "\033[1;30m", 7);
}

void		green(void)
{
	write(1, "\033[1;32m", 7);
}

void		reset(void)
{
	write(1, "\033[0m", 4);
}

void		cyan(void)
{
	write(1, "\033[1;36m", 7);
}
